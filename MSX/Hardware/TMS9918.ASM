FORCLR:               equ &F3E9
BAKCLR:               equ &F3EA
BDRCLR:               equ &F3EB
; =============================================================================
; Paleta do MSX
; =============================================================================
; 00	transparent
; 01	black
; 02	medium green
; 03	light green
; 04	dark blue
; 05	light blue
; 06	dark red
; 07	cyan
; 08	medium red
; 09	light red
; 10	dark yellow
; 11	light yellow
; 12	dark green
; 13	magenta
; 14	gray
; 15	white
; =============================================================================

; =============================================================================
; SetForeground - Muda a cor do texto - Modo de video texto
; =============================================================================
; A => nova cor (ver paleta)
; =============================================================================
; Altera => FORCLR (Variavel da BIOS)
; =============================================================================
SetForeground:
  ld (FORCLR),a
  call CHGCLR
ret

; =============================================================================
; SetBackground - Muda a cor do Fundo - Modo de video texto
; =============================================================================
; A => nova cor (ver paleta)
; =============================================================================
; Altera => BAKCLR (Variavel da BIOS)
; =============================================================================
SetBackground:
  ld (BAKCLR),a
  call CHGCLR
ret

; =============================================================================
; SetBorder - Muda a cor da Borda - Modo de video texto
; =============================================================================
; A => nova cor (ver paleta)
; =============================================================================
; Altera => BDRCLR (Variavel da BIOS)
; =============================================================================
SetBorder:
  ld (BDRCLR),a
  call CHGCLR
ret
